public class EshopModel {
  private Integer honeyCake;
  private Integer chocolateCake;
  private Integer marzipanCake;
  private Integer blueberryPie;
  private Integer cinnamonSnail;
  private String output;

  public Integer getHoneyCake() {
    return honeyCake;
  }

  public void setHoneyCake(Integer honeyCake) {
    this.honeyCake = honeyCake;
  }

  public Integer getChocolateCake() {
    return chocolateCake;
  }

  public void setChocolateCake(Integer chocolateCake) {
    this.chocolateCake = chocolateCake;
  }

  public Integer getMarzipanCake() {
    return marzipanCake;
  }

  public void setMarzipanCake(Integer marzipanCake) {
    this.marzipanCake = marzipanCake;
  }

  public Integer getBlueberryPie() {
    return blueberryPie;
  }

  public void setBlueberryPie(Integer blueberryPie) {
    this.blueberryPie = blueberryPie;
  }

  public Integer getCinnamonSnail() {
    return cinnamonSnail;
  }

  public void setCinnamonSnail(Integer cinnamonSnail) {
    this.cinnamonSnail = cinnamonSnail;
  }

  public String getOutput() {
    return output;
  }

  public void setOutput(String output) {
    this.output = output;
  }
}
